class Point2d_int:
    x: int
    y: int

    def set_point(self, x: int, y: int):
        self.x = x
        self.y = y


class Point2d_float:
    x: float
    y: float

    def set_point(self, x: float, y: float):
        self.x = x
        self.y = y


class Point3d_int:
    x: int
    y: int
    z: int

    def set_point(self, x: int, y: int, z: int):
        self.x = x
        self.y = y
        self.z = z


class Point3d_float:
    x: float
    y: float
    z: float

    def set_point(self, x: float, y: float, z: float):
        self.x = x
        self.y = y
        self.z = z
